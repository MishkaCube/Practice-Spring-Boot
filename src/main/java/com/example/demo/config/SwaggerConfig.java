package com.example.demo.config;

import io.swagger.v3.oas.models.Components;
import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.info.Info;
import io.swagger.v3.oas.models.security.SecurityRequirement;
import io.swagger.v3.oas.models.security.SecurityScheme;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;


/**
 * Класс отвечающий за конфигурацию OpenAPI (Swagger)
 */

@Configuration
public class SwaggerConfig {


    /**
     * @return object OpenAPI
     * Конфигурация включает дополнительный UI компонент для внесения JWT
     */
    @Bean
    public OpenAPI customOpenAPI() {
        final String securitySchemeName = "bearerAuth";
        return new OpenAPI()
                .addSecurityItem(new SecurityRequirement().addList("bearer"))
                .components(
                        new Components()
                                .addSecuritySchemes("bearer",
                                        new SecurityScheme()
                                                .name(securitySchemeName)
                                                .type(SecurityScheme.Type.HTTP)
                                                .scheme("bearer")
                                                .bearerFormat("JWT")
                                )
                )
                .info(new Info().title("API компании DevCloudy")
                        .description("Работа с разработчиками компании")
                        .version("v0.0.1"));
    }

}